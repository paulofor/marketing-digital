package gerador.testeselenium.passo.exemplos;

public class ItemStyle {

	private String idItem;
	private String style;
	
	public ItemStyle(String id, String style) {
		this.idItem = id;
		this.style = style;
	}

	public String getIdItem() {
		return idItem;
	}

	public String getStyle() {
		return style;
	}
	
	
}
